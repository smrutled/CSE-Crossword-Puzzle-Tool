    private void createUserBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserBActionPerformed
        Boolean errored=false;
        if(!errorUserLabel.getText().equals("")){
            errorUserLabel.setText("Username Exists");
            errored=true;
        }
        if(!Arrays.equals(newPasswordField.getPassword(),newConPasswordField.getPassword())){
            errorPassLabel.setText("Does not match");
            errored=true;
        }
        if(!errored){
        UserDataHandler.CreateUser(newUsernameText.getText(), newPasswordField.getPassword());
        newUserDialog.setVisible(false);
        }

    }//GEN-LAST:event_createUserBActionPerformed

