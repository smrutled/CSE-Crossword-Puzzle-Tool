    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        GUIManager manager = GUIManager.getGUIManager();
        if(userNameText.getText().equals("admin") && "123A321".equals(String.valueOf(passwordText.getPassword())) && !parameter){
            manager.ReplacePanel(new AdminPanel(), 400  , 650);
        }
        else{

        manager.currentUser[player]=UserDataHandler.GetUserData(userNameText.getText(), passwordText.getPassword());
        if(manager.currentUser[player]!=null && !parameter){
        loginCompText.setText("Login Complete");
        manager.ReplacePanel(new MenuPanel(),400,300); 
        }
        else if(manager.currentUser[player]!=null && parameter){
            loginCompText.setText("Login Complete");
            login=true; 
        }
        else{
            loginCompText.setText("Login Failed");
        }
        }
        
        
        
    }//GEN-LAST:event_loginButtonActionPerformed

